package br.com.imobiliaria.model;

public class Calculos {
    public double calcularAreaQuadrado(double lado) {
        return lado * lado;
    }
    public double calcularAreaRetangulo(double lado, double ladob){
        return lado * ladob;
    }
    public double calcularAreaTriangulo(double lado, double ladob){
        return lado * ladob;
    }
    public double calcularAreaCirculo(double lado, double ladob){
        return lado * ladob;
    }
    public double calcularAreaLosango(double lado, double ladob){
        return lado * ladob;
    }
}
